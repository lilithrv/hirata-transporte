    private void cargarComboPatentes() {
        cmbPatente.removeAllItems();
        cmbPatente.addItem("Seleccionar");
        mapaVehiculos.clear();

        // Solo vehículos SIN mantenimiento programado actualmente
        List<Vehiculo> vehiculos = mantenimientoDAO.listarSinMantenimientoProgramado();
        for (Vehiculo v : vehiculos) {
            mapaVehiculos.put(v.getPatente(), v);  // guarda patente: vehiculo
            cmbPatente.addItem(v.getPatente());
        }
    }

