    private void cmbPatenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPatenteActionPerformed
        // TODO add your handling code here:
        String patenteSeleccionada = (String) cmbPatente.getSelectedItem();

        if (patenteSeleccionada != null && !patenteSeleccionada.equals("Seleccionar")) {
            Vehiculo seleccionado = mapaVehiculos.get(patenteSeleccionada);
            Kilometraje ultimoRegistro = kilometrajeDAO.ultimoKmPorVehiculo(seleccionado.getIdVehiculo());

            txtMarcaC.setText(seleccionado.getMarca());
            txtModeloC.setText(seleccionado.getModelo());
            txtAnioC.setText(String.valueOf(seleccionado.getAnio()));
            
            // datos por defecto en este caso
            txtTipoC.setText("Correctivo");
            txtOrigenC.setText("Manual");
            
            // último km registrado, si no tiene, se deja el inicial
            txtKmC.setText(ultimoRegistro != null ? String.valueOf(ultimoRegistro.getKilometros()) : String.valueOf(seleccionado.getKilometrajeInicial()));

            // no editables
            txtMarcaC.setEditable(false);
            txtModeloC.setEditable(false);
            txtAnioC.setEditable(false);
            txtTipoC.setEnabled(false);
            txtOrigenC.setEnabled(false);
            txtKmC.setEditable(false);

        } else {
            // Limpiar si vuelven a "Seleccionar"
            txtMarcaC.setText("");
            txtModeloC.setText("");
            txtAnioC.setText("");
        }
    }//GEN-LAST:event_cmbPatenteActionPerformed

    private void btnProgramarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProgramarActionPerformed
        // TODO add your handling code here:
        String patenteSeleccionada = (String) cmbPatente.getSelectedItem();
   
        if (patenteSeleccionada == null || patenteSeleccionada.equals("Seleccionar")) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una patente.");
            return;
        }

        try {
            Vehiculo seleccionado = mapaVehiculos.get(patenteSeleccionada);
            
            int km = Integer.parseInt(txtKmC.getText().trim());

            Mantenimiento mant = new Mantenimiento();
            mant.setVehiculo(seleccionado);
            mant.setTipoMantenimiento(TipoMantenimiento.Correctivo);
            mant.setOrigen(OrigenMantenimiento.Manual); 
            mant.setKilometraje(km);

            mantenimientoDAO.correctivo(mant);

            JOptionPane.showMessageDialog(this, "Mantenimiento programado correctamente.");
            this.dispose(); // cierra el diálogo

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El kilometraje debe ser un número válido.");
        }
    }//GEN-LAST:event_btnProgramarActionPerformed

